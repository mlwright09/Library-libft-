#include "libft.h"

void	*ft_reverse(char *a)
{
	int		i;
	int		j;
	char	b;

	i = 0;
	j = ft_strlen(a) - 1;
	while (i < j)
	{
		b = a[i];
		a[i] = a[j];
		a[j] = b;
		i++;
		j--;
	}
	return (a);
}
